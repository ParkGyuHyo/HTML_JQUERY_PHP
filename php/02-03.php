<h3>===== 1~10 정수 출력 (for) =====</h3>
<?
	for($i=1; $i<=10; $i++){
		echo $i."<br/>";
	}
?>

<h3>===== 1~10 정수 출력 (while) =====</h3>
<?
	$i = 1;
	while($i<=10){
		echo $i."<br/>";
		$i++;
	}
?>

<h3>===== 10~1 정수 출력 (for) =====</h3>
<?
	for($i=10; $i>=1; $i--){
		echo $i."<br/>";
	}
?>

<h3>===== 10~1 정수 출력 (while) =====</h3>
<?
	$i = 10;
	while($i>=1){
		echo $i."<br/>";
		$i--;
	}
?>