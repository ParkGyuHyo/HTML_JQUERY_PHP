<h3>===== 1~10 홀수 출력 (for) =====</h3>
<?
	for($i=1; $i<=10; $i++){
		if ($i % 2 != 0){
			echo $i."<br/>";
		}
	}
?>

<h3>===== 1~10 홀수 출력 (while) =====</h3>
<?
	$i = 1;
	while($i<=10){
		if ($i % 2 != 0){
			echo $i."<br/>";
		}
		$i++;
	}
?>

<h3>===== 1~10 짝수 출력 (for) =====</h3>
<?
	for($i=1; $i<=10; $i++){
		if ($i % 2 == 0){
			echo $i."<br/>";
		}
	}
?>

<h3>===== 1~10 짝수 출력 (while) =====</h3>
<?
	$i = 1;
	while($i<=10){
		if ($i % 2 == 0){
			echo $i."<br/>";
		}
		$i++;
	}
?>